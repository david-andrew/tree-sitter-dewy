      if (lookahead == '-') ADVANCE(5);
      if (lookahead == '<') ADVANCE(79);
      if (lookahead == '=') ADVANCE(8);
      if (lookahead == '?') ADVANCE(76);
      if (lookahead == '|') ADVANCE(61);
      END_STATE();
    case 3:
      if (lookahead == '=') ADVANCE(9);
      if (lookahead == '>') ADVANCE(80);
      if (lookahead == '?') ADVANCE(75);
